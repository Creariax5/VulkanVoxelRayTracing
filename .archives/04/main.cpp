void move(int key) {
    std::cout << camOri.x << std::endl;
    if (key == GLFW_KEY_W) {
        camCoo = glm::vec3(camCoo.x + glm::sin(camOri.y)/4, camCoo.y + glm::cos(camOri.y)/4, camCoo.z);
    }
    if (key == GLFW_KEY_S) {
        camCoo = glm::vec3(camCoo.x - glm::sin(camOri.y)/4, camCoo.y - glm::cos(camOri.y)/4, camCoo.z);
    }

    if (key == GLFW_KEY_A) {
        camCoo = glm::vec3(camCoo.x - glm::sin(camOri.y + PI/2)/4, camCoo.y - glm::cos(camOri.y + PI/2)/4, camCoo.z);
    }
    if (key == GLFW_KEY_D) {
        camCoo = glm::vec3(camCoo.x + glm::sin(camOri.y + PI/2)/4, camCoo.y + glm::cos(camOri.y + PI/2)/4, camCoo.z);
    }

    if (key == GLFW_KEY_E) {
        camCoo = glm::vec3(camCoo.x, camCoo.y, camCoo.z -1);
    }

    if (key == GLFW_KEY_SPACE) {
        camCoo = glm::vec3(camCoo.x, camCoo.y, camCoo.z +1);
    }
}

